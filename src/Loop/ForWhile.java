public class ForWhile {
    public static void main(String[] args) {
    System.out.println("Using for loop:");
    for (int i = 1; i <= 10; i++) {
        System.out.print(i + " ");
    }
    System.out.println();
    System.out.println("Using while loop:");
    int j = 1;
    while (j <= 10) {
        System.out.print(j + " ");
        j++;
    }
    }
}
